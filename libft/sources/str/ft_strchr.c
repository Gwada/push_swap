#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] && s[i] != c)
		i++;
	if (!c || c == s[i])
		return ((char*)(s + i));
	return (NULL);
}
